#include "../LIB/cub3d.h"

int	ft_not_allowed_in_rgb(int i, int j, char **rgb)
{
	if ((rgb[i][j] > 32 && rgb[i][j] < 48)
		|| (rgb[i][j] > 57 && rgb[i][j] < 127))
		return (1);
	return (0);
}